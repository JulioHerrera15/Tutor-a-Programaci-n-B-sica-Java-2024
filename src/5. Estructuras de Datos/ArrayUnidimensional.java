public class ArrayUnidimensional {
    public static void main(String[] args) {
        // Definir y asignar valores a un array unidimensional
        int[] arrayUnidimensional = {10, 20, 30, 40, 50};
        
        // Imprimir los valores según su índice
        System.out.println("Array unidimensional:");
        System.out.println("Primera Posición: " + arrayUnidimensional[0]);
        System.out.println("Segunda Posición: " + arrayUnidimensional[1]);
        System.out.println("Tercera Posición: " + arrayUnidimensional[2]);
        System.out.println("Cuarta Posición: " + arrayUnidimensional[3]);
        System.out.println("Quinta Posición: " + arrayUnidimensional[4]);
    }
}
