// 1. Tipos de datos

public class TiposDeDatos {
    public static void main(String[] args) {
        // Tipos de datos primitivos
        int entero = 10; // Número entero
        double decimal = 20.5; // Número decimal
        char caracter = 'A'; // Un solo carácter
        boolean booleano = true; // Verdadero o falso
        
        // Tipos de datos de referencia (objetos)
        String texto = "Hola, mundo"; // Cadena de texto (no es primitivo, es un objeto)
        
        System.out.println("Entero: " + entero);
        System.out.println("Decimal: " + decimal);
        System.out.println("Carácter: " + caracter);
        System.out.println("Booleano: " + booleano);
        System.out.println("Texto: " + texto);
    }
}



